    private void autonumber() {
        Connection connection = null;
        Statement stmt = null;
        ResultSet result = null;

        try {
            // Membuat query untuk mengambil id terakhir yang digunakan
            String query = "SELECT * FROM login ORDER BY id DESC LIMIT 1";

            // Mendapatkan koneksi
            connection = SQLConnection.getConnection();
            stmt = connection.createStatement();

            // Eksekusi query
            result = stmt.executeQuery(query);

            if (result.next()) {
                // Ambil ID terakhir dan hapus prefix "ID"
                String NoID = result.getString("id").substring(2);
                String ID = "" + (Integer.parseInt(NoID) + 1); // Increment ID

                // Tentukan jumlah angka leading zeros
                String Zero = "";
                if (ID.length() == 1) {
                    Zero = "00";
                } else if (ID.length() == 2) {
                    Zero = "0";
                }

                // Set ID baru ke field
                txtid.setText("ID" + Zero + ID);
            } else {
                // Jika tidak ada data, ID pertama adalah "ID001"
                txtid.setText("ID001");
            }
        } catch (Exception e) {
            // Jika terjadi error, cetak pesan error
            System.out.println("Auto Number error: " + e.getMessage());
            e.printStackTrace();
        } finally {
            // Tutup koneksi, statement, dan result set
            try {
                if (result != null) {
                    result.close();
                }
                if (stmt != null) {
                    stmt.close();
                }
                if (connection != null) {
                    connection.close();
                }
            } catch (SQLException e) {
                e.printStackTrace();
            }
        }
    }

