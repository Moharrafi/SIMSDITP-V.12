    private void btnregister1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnregister1ActionPerformed
        String id = txtid.getText().trim();
        String username = txtnama.getText().trim();
        String email = txtemail.getText().trim();
        String level = cblevel.getSelectedItem().toString().trim();

        // Menggunakan char[] untuk mengambil data password dari JPasswordField
        char[] passwordArray = txtpass.getPassword();
        char[] ulangiPasswordArray = txtrepass.getPassword();

        // Konversi char[] ke String untuk validasi
        String password = new String(passwordArray).trim();
        String ulangiPassword = new String(ulangiPasswordArray).trim();

        // Validasi input
        if (password.isEmpty() || username.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Username atau Password tidak boleh kosong", "Peringatan", JOptionPane.WARNING_MESSAGE);
        } else if (!password.equals(ulangiPassword)) {
            JOptionPane.showMessageDialog(null, "Password tidak sama");
        } else {
            try {
                // Query untuk memasukkan data ke tabel login
                String query = "INSERT INTO login (id, username,email, level, password) VALUES (?, ?, ?, ?, ?)";

                // Gunakan metode untuk eksekusi query
                boolean success = SQLConnection.doPreparedUpdate(query, id, username, email, level, password);
                if (success) {
                    Register.showSuccessDialog("Buat Akun Berhasil");
                    this.dispose(); // Tutup form setelah berhasil
                    Login loginForm = new Login();
                    loginForm.setVisible(true); // Buka form login
                } else {
                    Register.showErrorDialog("Gagal membuat akun");
                }
            } catch (SQLException e) {
                Register.showErrorDialog("Terjadi kesalahan: " + e.getMessage());
                e.printStackTrace();
            } finally {
                // Hapus data password dari memori setelah digunakan
                java.util.Arrays.fill(passwordArray, '\0');
                java.util.Arrays.fill(ulangiPasswordArray, '\0');
            }
        }
    }//GEN-LAST:event_btnregister1ActionPerformed

